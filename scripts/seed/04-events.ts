const EXPANSION_EVENTS: SeedEvent[] = [
  ...PAST_EVENTS,
  ...LIVE_EVENTS,
  ...NEAR_TERM_EVENTS,
];

// ─── Seed ────────────────────────────────────────────────────────────────

export async function seedEvents(db: Firestore): Promise<number> {
  const all = [...LEGACY_EVENTS, ...EXPANSION_EVENTS];

  await Promise.all(
    all.map((event) => db.collection("events").doc(event.id).set(event)),
  );

  return all.length;
}
